/**
 Purpose: A class call Merchandise to define the information of each merchandise
 */
package company.store;

public class Merchandise {
    // merchandise name
    public String name;
    // merchandise in-store id
    public String id;
    // how many merchandise
    public int count;
    // each merchandise sold price
    public double soldPrice;
    // each merchandise purchase price
    public double purchasePrice;
}
